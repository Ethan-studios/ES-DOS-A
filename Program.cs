        static void Directories()
        {
            Console.WriteLine("Type the name to make a directory: ");
            string folderName = Console.ReadLine();

            Console.WriteLine("Now type the path: ");
            Console.WriteLine("Example: C:\\folder\name of the directory");
            string path = Console.ReadLine();

            if (string.IsNullOrEmpty(path))
            {
                path = Directory.GetCurrentDirectory();
            }

            string fullPath = Path.Combine(path, folderName);

            try
            {
                Directory.CreateDirectory(fullPath);
                Console.WriteLine($"Directory '{folderName}' created on: {fullPath}");
            }
            catch (Exception ex)
            {
                Console.WriteLine($"Error: {ex.Message}");
            }
        }
